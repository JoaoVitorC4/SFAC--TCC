        public void pesquisarPlano(){
        
            PlanoCTR objplano = new PlanoCTR();
            
            rsplano = objplano.PesquisarPLANOCTR(txtPesquisar.getText());
        
            preenche_conplano();
        
        }
    
    
    public void preenche_conplano(){
        
        String [] colunas = {"Código" ,"Nome","Valor do Plano","Quantidade de Dependentes", "Quantidade de Convidados"};
        String [][] linhas = {};
        
            DefaultTableModel tablemodel = new DefaultTableModel(linhas,colunas)
        {
            public boolean CelulaEditavel(int rowIndex, int mColIndex)
            {
                return false;
            }
        };
    
            Vector < Vector> dados = new Vector();
    
        try {
            while (rsplano.next())
            {
               Vector regVetor = new Vector();
               
               regVetor.add(rsplano.getInt("cod_plano"));
                regVetor.add(rsplano.getString("nome_plano"));             
                regVetor.add(rsplano.getFloat("valor_plano"));             
                regVetor.add(rsplano.getInt("qtd_dependente"));             
                regVetor.add(rsplano.getInt("qtd_convidado"));             
                   
               dados.add(regVetor);
               tablemodel.addRow(regVetor);
            }  
            tblPlano.setModel(tablemodel);
            
        } catch (SQLException ex) {
            Logger.getLogger(Psq_PlanoVIEW.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    public void ExcluirPlano()
    {
        PlanoCTR objgru = new PlanoCTR();
        
        int linha = tblPlano.getSelectedRow();
            
        objplano.cod_plano = (int) tblPlano.getValueAt(linha, 0);
        
        
        objgru.ExcluiPlanoCTR(objplano.cod_plano);
    }
    
       private static class RoundedBorder implements Border {
        
        private int radius;
        
        RoundedBorder(int radius) {
            this.radius = radius;
        }
        public Insets getBorderInsets(Component c) {
            return new Insets(this.radius+1, this.radius+1, this.radius+2, this.radius);
        }

        public boolean isBorderOpaque() {
            return true;
        }

        public void paintBorder(Component c, Graphics g, int x, int y, int width, int height) {
            g.drawRoundRect(x,y,width-1,height-1,radius,radius);
        }
    }
    
    
